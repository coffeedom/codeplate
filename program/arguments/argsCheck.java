
class argsCheck
{
	public static void main(String args[])
	{
		System.out.println("");
		System.out.println("args        = " + args);
		System.out.println("args.length = " + args.length);

		for (int i=0; i < args.length; i++)
		{
		    System.out.println("args[  " + i + "  ] = " + args[i]);
		}
		System.out.println("");
	}
}


